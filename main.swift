// Add your code below:
func selectionSort(_ array: [Int]) {

    var a = array  
    print("Pass: \(0), Swaps: \(0)/\(0), Array: \(a)")
    for x in 0 ..< a.count - 1 {

        var lowest = x
        for y in x + 1 ..< a.count {
            if a[y] < a[lowest] {
                lowest = y
            }
        }
        if x != lowest {            
            a.swapAt(x, lowest)
        }
        print("Pass: \(x+1), Swaps: \(1)/\(x+1), Array: \(a)")
    }
}
selectionSort(unsortedIntegers)

